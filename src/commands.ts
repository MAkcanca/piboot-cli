import { existsSync, readFileSync, writeFileSync, mkdirSync, rmSync, readdirSync, chownSync, chmodSync } from "node:fs";
import { execFileSync } from "node:child_process";
import { join, basename } from "node:path";
import { type Config, type PiNode, saveConfig, tftpDir, nfsDir } from "./config.ts";
import { $, $quiet, $try, log, sleep, onCleanup, removeCleanup } from "./shell.ts";
import { configureNetwork } from "./network.ts";

// ── Shared helpers ──────────────────────────────────────────────────────────

function cmdline(config: Config, nfsPath: string): string {
  return `console=serial0,115200 console=tty1 root=/dev/nfs nfsroot=${config.lan_ip}:${nfsPath},nfsvers=3 rw ip=dhcp rootwait`;
}

function fstab(config: Config, nfsPath: string, tftpPath: string): string {
  return `# Netboot fstab — generated by piboot
proc            /proc   proc    defaults          0 0
${config.lan_ip}:${nfsPath}  /     nfs  defaults,noatime,nfsvers=3  0 1
${config.lan_ip}:${tftpPath}  /boot/firmware  nfs  defaults,noatime,nfsvers=3  0 2
tmpfs           /tmp    tmpfs   defaults,nosuid   0 0
tmpfs           /var/tmp tmpfs  defaults,nosuid   0 0
`;
}

function writeDnsmasqConf(config: Config): void {
  const nodeLeases = config.nodes
    .map((n) => `dhcp-host=${n.mac},${n.ip},${n.hostname},infinite`)
    .join("\n");

  writeFileSync("/etc/dnsmasq.conf",
`# ── Interface ────────────────────────────────
interface=${config.lan_if}
bind-interfaces
except-interface=lo

# ── DNS (forward upstream) ───────────────────
server=1.1.1.1
server=8.8.8.8

# ── DHCP ─────────────────────────────────────
dhcp-range=${config.dhcp_range_start},${config.dhcp_range_end},${config.lan_mask},${config.dhcp_lease}
dhcp-option=option:router,${config.lan_ip}
dhcp-option=option:dns-server,${config.lan_ip}

# ── Static leases ────────────────────────────
${nodeLeases}

# ── TFTP / PXE (Raspberry Pi netboot) ───────
enable-tftp
tftp-root=${config.tftp_root}
pxe-service=0,"Raspberry Pi Boot"

# ── Logging ──────────────────────────────────
log-dhcp
log-facility=/var/log/dnsmasq.log
`);
}

function writeNfsExports(config: Config): void {
  const entries = config.nodes.flatMap((n) => {
    const nfs = nfsDir(config, n.hostname);
    const tftp = tftpDir(config, n.serial);
    return [
      `${nfs}   ${config.lan_subnet}.0/24(rw,sync,no_subtree_check,no_root_squash)`,
      `${tftp}  ${config.lan_subnet}.0/24(rw,sync,no_subtree_check,no_root_squash)`,
    ];
  });

  writeFileSync("/etc/exports",
`# Netboot exports — managed by piboot
${entries.join("\n")}
`);
}

async function ensureIptablesRule(rule: string): Promise<void> {
  const result = await $try(`iptables -C ${rule} 2>/dev/null && echo EXISTS`);
  if (result.trim() === "EXISTS") return;
  await $(`iptables -A ${rule}`);
}

function findSshPubKey(path?: string): string | null {
  if (path && path !== "true") {
    if (!existsSync(path)) log.fail(`SSH key not found: ${path}`);
    return readFileSync(path, "utf-8").trim();
  }
  // Auto-detect: try common public key locations
  const home = process.env.HOME ?? "/root";
  for (const name of ["id_ed25519.pub", "id_rsa.pub", "id_ecdsa.pub"]) {
    const p = join(home, ".ssh", name);
    if (existsSync(p)) {
      log.info(`Using SSH key: ${p}`);
      return readFileSync(p, "utf-8").trim();
    }
  }
  log.fail(
    `No SSH public key found. Generate one with: ssh-keygen -t ed25519\n` +
    `       Or specify a path: --ssh-key /path/to/key.pub`
  );
}

/** Find the server's SSH public key (for management access to nodes) */
function findServerKey(): string | null {
  const home = process.env.HOME ?? "/root";
  for (const name of ["id_ed25519.pub", "id_rsa.pub", "id_ecdsa.pub"]) {
    const p = join(home, ".ssh", name);
    if (existsSync(p)) return readFileSync(p, "utf-8").trim();
  }
  return null;
}

function installSshKeys(nfsRoot: string, userKey?: string | null): void {
  const keys: string[] = [];

  // Always install the server's key for management (reboot, diagnostics)
  const serverKey = findServerKey();
  if (serverKey) keys.push(serverKey);

  // Install user-provided key if different from server key
  if (userKey && userKey !== serverKey) keys.push(userKey);

  if (keys.length === 0) return;

  const sshDir = join(nfsRoot, "home/pi/.ssh");
  mkdirSync(sshDir, { recursive: true });
  writeFileSync(join(sshDir, "authorized_keys"), keys.join("\n") + "\n");

  // Set ownership to pi (uid 1000) — chown works on the NFS root directly
  chownSync(sshDir, 1000, 1000);
  chmodSync(sshDir, 0o700);
  chownSync(join(sshDir, "authorized_keys"), 1000, 1000);
  chmodSync(join(sshDir, "authorized_keys"), 0o600);

  // Disable password auth when key auth is available
  const sshdConf = join(nfsRoot, "etc/ssh/sshd_config.d/piboot.conf");
  mkdirSync(join(nfsRoot, "etc/ssh/sshd_config.d"), { recursive: true });
  writeFileSync(sshdConf, "PasswordAuthentication no\n");

  log.info(`SSH key auth configured (${keys.length} key${keys.length > 1 ? "s" : ""}), password auth disabled`);
}

async function patchNfsRoot(config: Config, node: PiNode, sshKey?: string | null): Promise<void> {
  const nfs = nfsDir(config, node.hostname);
  const tftp = tftpDir(config, node.serial);

  // fstab
  writeFileSync(join(nfs, "etc/fstab"), fstab(config, nfs, tftp));

  // hostname
  writeFileSync(join(nfs, "etc/hostname"), node.hostname + "\n");
  const hosts = readFileSync(join(nfs, "etc/hosts"), "utf-8");
  writeFileSync(
    join(nfs, "etc/hosts"),
    hosts.replace(/127\.0\.1\.1.*/g, `127.0.1.1\t${node.hostname}`)
  );

  // SSH — enable and pre-generate host keys so sshd works immediately on boot
  writeFileSync(join(nfs, "boot/firmware/ssh"), "");
  writeFileSync(join(tftp, "ssh"), "");
  for (const type of ["rsa", "ecdsa", "ed25519"]) {
    const keyPath = join(nfs, `etc/ssh/ssh_host_${type}_key`);
    if (!existsSync(keyPath)) {
      await $quiet(`ssh-keygen -t ${type} -f "${keyPath}" -N "" -q`);
    }
  }

  // Default user: pi/raspberry
  const hash = await $quiet("echo 'raspberry' | openssl passwd -6 -stdin");
  writeFileSync(join(tftp, "userconf.txt"), `pi:${hash}\n`);
  writeFileSync(join(nfs, "boot/firmware/userconf.txt"), `pi:${hash}\n`);

  // Strip init= from cmdline
  if (existsSync(join(tftp, "cmdline.txt"))) {
    const cl = readFileSync(join(tftp, "cmdline.txt"), "utf-8");
    writeFileSync(join(tftp, "cmdline.txt"), cl.replace(/ init=[^ ]*/g, ""));
  }

  // Disable SD-card services
  for (const svc of ["resize2fs_once", "dphys-swapfile"]) {
    const wantsLink = join(nfs, `etc/systemd/system/multi-user.target.wants/${svc}.service`);
    const maskLink = join(nfs, `etc/systemd/system/${svc}.service`);
    if (existsSync(wantsLink)) rmSync(wantsLink, { force: true });
    await $try(`ln -sf /dev/null "${maskLink}"`);
  }

  // First-boot auto-update service
  const firstBootService = `[Unit]
Description=First boot system update
After=network-online.target
Wants=network-online.target
ConditionPathExists=/var/lib/piboot-first-boot

[Service]
Type=oneshot
ExecStart=/bin/bash -c 'apt-get update && apt-get upgrade -y && rm /var/lib/piboot-first-boot'
ExecStart=/bin/systemctl disable piboot-first-update.service

[Install]
WantedBy=multi-user.target
`;
  writeFileSync(join(nfs, "etc/systemd/system/piboot-first-update.service"), firstBootService);
  writeFileSync(join(nfs, "var/lib/piboot-first-boot"), ""); // marker file
  // Enable the service
  mkdirSync(join(nfs, "etc/systemd/system/multi-user.target.wants"), { recursive: true });
  await $try(`ln -sf /etc/systemd/system/piboot-first-update.service "${join(nfs, "etc/systemd/system/multi-user.target.wants/piboot-first-update.service")}"`);

  // SSH key auth — always install server key for management, plus user key if provided
  installSshKeys(nfs, sshKey);

  // Passwordless sudo for pi user (needed for remote reboot)
  writeFileSync(join(nfs, "etc/sudoers.d/010_pi-nopasswd"), "pi ALL=(ALL) NOPASSWD: ALL\n");
  chmodSync(join(nfs, "etc/sudoers.d/010_pi-nopasswd"), 0o440);

  log.info(`Patches applied → ${node.hostname}`);
}

/** Find serial by scanning TFTP dirs for cmdline.txt referencing the hostname */
function findSerialByHostname(config: Config, hostname: string): string | null {
  if (!existsSync(config.tftp_root)) return null;

  const dirs = readdirSync(config.tftp_root, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name);

  for (const serial of dirs) {
    const cmdlinePath = join(config.tftp_root, serial, "cmdline.txt");
    if (existsSync(cmdlinePath)) {
      const content = readFileSync(cmdlinePath, "utf-8");
      // cmdline contains nfsroot=IP:/srv/nfs/HOSTNAME
      if (content.includes(`/${hostname},`) || content.includes(`/${hostname} `)) {
        return serial;
      }
    }
  }
  return null;
}

async function extractImage(config: Config, targetNfs: string, targetTftp: string): Promise<void> {
  if (!existsSync(config.image_raw)) {
    log.fail(`Base image not found: ${config.image_raw} — run 'piboot init' first`);
  }

  const loopDev = await $quiet(`losetup --show -fP "${config.image_raw}"`);
  await $(`kpartx -a "${loopDev}"`, { silent: true });
  const loopBase = basename(loopDev);
  await sleep(1000);

  const bootPart = `/dev/mapper/${loopBase}p1`;
  const rootPart = `/dev/mapper/${loopBase}p2`;

  let extractionComplete = false;

  const cleanup = async () => {
    await $try('umount /mnt/rpi_boot 2>/dev/null');
    await $try('umount /mnt/rpi_root 2>/dev/null');
    await $try(`kpartx -d "${loopDev}"`);
    await $try(`losetup -d "${loopDev}"`);

    // Remove partial directories if extraction didn't finish
    if (!extractionComplete) {
      if (existsSync(targetNfs)) {
        rmSync(targetNfs, { recursive: true, force: true });
        log.warn(`Removed partial NFS root: ${targetNfs}`);
      }
      if (existsSync(targetTftp)) {
        rmSync(targetTftp, { recursive: true, force: true });
        log.warn(`Removed partial TFTP dir: ${targetTftp}`);
      }
    }
  };
  onCleanup(cleanup);

  try {
    mkdirSync("/mnt/rpi_boot", { recursive: true });
    mkdirSync("/mnt/rpi_root", { recursive: true });
    await $(`mount -o ro "${rootPart}" /mnt/rpi_root`, { silent: true });
    await $(`mount -o ro "${bootPart}" /mnt/rpi_boot`, { silent: true });

    log.info("Copying rootfs (this takes a while) …");
    mkdirSync(targetNfs, { recursive: true });
    await $(`rsync -a /mnt/rpi_root/ "${targetNfs}/"`, { silent: true });

    mkdirSync(join(targetNfs, "boot/firmware"), { recursive: true });
    await $(`rsync -a /mnt/rpi_boot/ "${targetNfs}/boot/firmware/"`, { silent: true });

    log.info("Copying boot files …");
    mkdirSync(targetTftp, { recursive: true });
    await $(`rsync -a /mnt/rpi_boot/ "${targetTftp}/"`, { silent: true });

    extractionComplete = true;
  } finally {
    await cleanup();
    removeCleanup(cleanup);
  }
}

